type Details = {
  size: string;
  image: {
    width: number;
    height: number;
  };
};

type File = {
  url: string;
  details: Details;
};

type Image = {
  fields: {
    title: string;
    file: File;
  };
};

type Resource = {
  fields: {
    url: string;
    name: string;
  };
};

type ContentfulQuestion = {
  id: string;
  image: Image;
  date: string;
  partNumber: number;
  questionNumber: number;
  segment: string;
  resources: Resource[];
};

type EntryResponse = {
  items: {
    fields: ContentfulQuestion;
  }[];
};

export const fetchAllQuestions = async (): Promise<Question[]> => {
  console.log("fetching");
  return client
    .getEntries({
      content_type: "question",
    })
    .then((response: EntryResponse) => {
      return response.items
        .map((entry) => entry.fields)
        .map((question) => ({
          ...question,
          image: `https:${question.image.fields.file.url}`,
        }));
    });
};

